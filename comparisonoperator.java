public class comparisonoperator {
    public static void main(String[] args) {
        // comparison operators are ==,<,>,!=,<=,>=;
        int a = 10;
        int b = 20;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
