public class literals {
    public static void main(String[] args) {
        int count=256;
        System.out.println(count);
        int octa=057;
        System.out.println(octa);
        int hexa=0x07;
        System.out.println(hexa); 
        int binary=0b001010;
        System.out.println(binary); 
        char ch1='z';
        System.out.println(ch1);
        char ch2='\u0021';
        System.out.println(ch2);
        boolean bool=true;
        System.out.println(bool);
        String str1="hello world";
        System.out.println(str1);
        String str2="Null";
        System.out.println(str2);

    }
   
}
//literals are value.types of literals-integer,boolean,string,character.sub types of literals are decimal integer(0-9),octal integer(0-7)(always starts with 0),hexa decimal integer(0-9 and 10-15 represented by A-F)(started with 0X or 0x),binary integer(0 and 1)(starts with 0b).
//sub tyes of literals-string literals-written under "" are floating point literals,boolean literals,class literals,null literals.
